package Modul5.Percobaan;

public class Array {
    public static void main(String[] args) {
        int[] data = { 20, 10, 50, 30, 10 };
        System.out.println("Isi data Anda adalah : ");
        for (int i = 0; i < data.length; i++) {
            System.out.print(data[i] + " ");
        }
        System.out.println();
    }
}
