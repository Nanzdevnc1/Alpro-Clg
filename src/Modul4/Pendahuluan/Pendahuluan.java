package Modul4.Pendahuluan;

public class Pendahuluan {
    public static void main(String[] args) {
        // While Loop
        int x = 1;
        while (x <= 10) {
            System.out.println(x);
            x++;
        }
        System.out.println();

        // Do-While Loop
        int y = 1;
        do {
            System.out.println(y);
            y++;
        } while (y <= 10);
        System.out.println();

        // For Loop
        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }
    }
}
